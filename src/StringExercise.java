public class StringExercise {
    public static void main(String[] args) {
        String theWall = "We don't need no education\nWe don't need no thought control";
        System.out.println(theWall);

        String checkit = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(checkit);

        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);

        String backslash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(backslash);
    }
}
